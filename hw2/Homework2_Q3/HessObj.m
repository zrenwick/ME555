function H = HessObj(x)
    x1 = x(1);
    x2 = x(2);
%     H = [2+800*x(1)^2+-400*(x(2)-x(1)^2),-400*x(1);
%         -400*x(1),200];
    H_11 = (-8*x1*x2*(2*x1^2 + 3*x2^2 - 48))/((16 - x1^2 - x2^2)^(3/2));
    H_22 = H_11;
    H_12 = (-8*(16 - 2*x1^2 - 3*x2^2)*(16 - x1^2 - x2^2) - 8*x2^2*(16 - 2*x1^2 - x2^2))/((16 - x1^2 - x2^2)^(3/2));
    H_21 = H_12;
    
    H = [ H_11, H_12;...
          H_21, H_22];